    private void bottaoCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottaoCadastrarActionPerformed
        // TODO add your handling code here:
        //Conex√£o e Instancia√ß√£o do Banco de Dados
        MySQL mysql = new MySQL("localhost:3306", "controletech", "root", "Bico1346@");
        //Realizando Conex√£o
        mysql.conectaBanco();
        
        Funcionario funcionario = new Funcionario();
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO(mysql);

        funcionario.setId(Integer.parseInt(textId.getText()));
        funcionario.setCpf(textCpf.getText());
        funcionario.setNome(textNome.getText());
        funcionario.setEmail(textEmail.getText());
        funcionario.setEndereco(textEndereco1.getText());
        
        try {
            funcionario.setData_nasc(DateUtils.stringToDate(textDataNasc.getText()));
            funcionario.setData_ingresso(DateUtils.stringToDate(textData.getText()));
        } catch (ParseException ex) {
            Logger.getLogger(javaFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        funcionario.setEndereco(textSalario.getText());
        funcionario.setSalario(Double.parseDouble(textSalario.getText()));
        
        try {
            funcionarioDAO.cadastrarFuncionario(funcionario);
            JOptionPane.showMessageDialog(rootPane, "Funcionario Cadastrado com sucesso!");
        } catch(SQLException  e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Erro ao Cadastrar Funcionario!");
        } finally {
            mysql.fechaBanco();
        }
    }//GEN-LAST:event_bottaoCadastrarActionPerformed

