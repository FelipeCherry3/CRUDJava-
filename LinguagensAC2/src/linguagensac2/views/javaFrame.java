    private void bottaoBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bottaoBuscarActionPerformed
        MySQL mysql = new MySQL("localhost:3306", "controletech", "root", "Bico1346@");
        mysql.conectaBanco();
        
        Funcionario funcionario = new Funcionario();
        FuncionarioDAO funcionarioDAO = new FuncionarioDAO(mysql);

        try {
            Funcionario f2 = funcionarioDAO.buscarFuncionarioPorCpf(textBuscar.getText());
            if(f2 != null) {
                textId.setText(String.valueOf(f2.getId()));
                textNome.setText(f2.getNome());
                textCpf.setText(f2.getCpf());
                textEmail.setText(f2.getEmail());
                textData.setText(DateUtils.dateToString(f2.getData_ingresso()));
                textEndereco1.setText(f2.getEndereco());
                textDataNasc.setText(DateUtils.dateToString(f2.getData_nasc()));
                textSalario.setText(String.valueOf(f2.getSalario()));
            } else {
                JOptionPane.showMessageDialog(null,"Funcion√°rio N√£o Encontrado");
                textBuscar.setText("");
            }
            
        } catch (SQLException ex) {
            Logger.getLogger(javaFrame.class.getName()).log(Level.SEVERE, null, ex);
            JOptionPane.showMessageDialog(null,"Erro ao Buscar Funcion√°rio");
        }
        
    }//GEN-LAST:event_bottaoBuscarActionPerformed

